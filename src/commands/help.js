function formatDateTime() {
  const now = new Date();

  const date = now.toLocaleDateString('pt-BR', {
    timeZone: 'America/Campo_Grande',
  });

  const time = now.toLocaleTimeString('pt-BR', {
    timeZone: 'America/Campo_Grande',
    hour: '2-digit',
    minute: '2-digit',
    second: '2-digit',
  });

  return { date, time };
}

